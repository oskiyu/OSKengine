#pragma once

#include <cstdint>

namespace OSK {

	/// @brief Índice de un elemento,
	/// almacenado en un unsigned int
	/// de 32 bits.
	using UIndex32 = uint32_t;

	/// @brief Tamaño,
	/// almacenado en un unsigned int
	/// de 32 bits.
	using USize32 = uint32_t;


	/// @brief Índice de un elemento,
	/// almacenado en un unsigned int
	/// de 64 bits.
	using UIndex64 = uint64_t;

	/// @brief Tamaño,
	/// almacenado en un unsigned int
	/// de 64 bits.
	using USize64 = uint64_t;


	/// @brief Delta de tiempo,
	/// medido en segundos.
	using TDeltaTime = float;


	/// @brief Unsigned byte.
	using TByte = uint8_t;

}
