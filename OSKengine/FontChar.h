#pragma once

#include "Sprite.h"
#include "Vector2.hpp"

namespace OSK {

	/// <summary>
	/// Carácter de una fuente.
	/// </summary>
	struct OSKAPI_CALL FontChar {
				
		Vector4i textureCoords;

		/// <summary>
		/// Tamaño del carácter.
		/// </summary>
		Vector2 size;

		/// <summary>
		/// Espaciado del carácter.
		/// </summary>
		Vector2 bearing;

		/// <summary>
		/// Tamaño horizontal del carácter.
		/// </summary>
		uint32_t advance = 0;

	};

}